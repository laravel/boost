## Flux UI Free
- Flux UI is the official Livewire component library - It has full access to the free components and variants, but does not have access to the Pro components.
- use `<flux:*>` components when available - they are the recommended way to build Livewire interfaces.
- **IMPORTANT**: Use/activate 'fluxui-development' for component usage and available components list whenever working with Flux UI components.
